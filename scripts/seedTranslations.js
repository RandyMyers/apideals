const seedTranslations = async () => {
  try {
    // Connect to MongoDB using the same method as app.js
    const connected = await connectDB();
    if (!connected) {
      console.error('Failed to connect to MongoDB. Exiting...');
      process.exit(1);
    }

    // Clear existing translations (optional - comment out if you want to keep existing)
    // await Translation.deleteMany({});
    // console.log('Cleared existing translations');

    // Insert translations
    let created = 0;
    let updated = 0;
    let skipped = 0;

    for (const translation of initialTranslations) {
      const existing = await Translation.findOne({ key: translation.key });
      
      if (existing) {
        // Update existing translation
        await Translation.findOneAndUpdate(
          { key: translation.key },
          { $set: translation },
          { runValidators: true }
        );
        updated++;
        console.log(`✓ Updated: ${translation.key}`);
      } else {
        // Create new translation
        await Translation.create(translation);
        created++;
        console.log(`✓ Created: ${translation.key}`);
      }
    }

    console.log('\n=== Seeding Complete ===');
    console.log(`Created: ${created}`);
    console.log(`Updated: ${updated}`);
    console.log(`Skipped: ${skipped}`);
    console.log(`Total: ${initialTranslations.length}`);

    // Close connection
    await mongoose.connection.close();
    console.log('\nMongoDB connection closed');
    process.exit(0);
  } catch (error) {
    console.error('Error seeding translations:', error);
    process.exit(1);
  }
};

// Run if called directly
if (require.main === module) {
  seedTranslations();
}

